def sumar(num1, num2):
    """ Esta funcion recibe dos numeros y devuelve la suma de ambos """
    return num1+num2

def restar(num1, num2):
    return num1-num2

def dividir(num1, num2):
    return num1/num2

def multiplicar(num1, num2):
    return num1*num2

